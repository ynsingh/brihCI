<b><h3>Following are some examples of Journal Entries:	</h3></b></br>

<u><b><h4>CASE I (Capital brought in organization / Contra)</h4></b></u></br>


<b>1. Funds granted as capital Rs. 300, 000</b></br></br>

&nbsp;&nbsp;&nbsp;&nbsp; a) What comes in organization will be debited </br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Cash has come in organization; cash account will be debited in journal entry.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; b) Fund account will be credited  </br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Funds are granted through different sources; corresponding fund account will be credited.</br></br>

<b><u>Journal Entry</u></br></br>

Cash Account Debit 300, 000</br></br>

Fund's Account Credit 300,000</br></br>

Narration : </b>Funds granted by xyz.</br></br></br></br>



<u><b><h4>CASE II (Asset purchased on credit / Deferred Payment)</h4></b></u><br>


<b>1. Asset purchased on credit</b></br></br>

&nbsp;&nbsp;&nbsp;&nbsp; a) What comes in organization will be debited</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Asset have come in organization, so its financial value will be debited with the name of asset's account. </br></br>

&nbsp;&nbsp;&nbsp;&nbsp; b) Name of person is given, from whom we bought the goods on credit.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Who is giver, will be credited.</br></br>

<b><u>Journal Entry</u></br></br>

Asset's Account Debit 20,000</br></br>

Sundry creditor's Account Credit 20,000</br></br>

Narration : </b>Asset purchased on credit from sundry creditors.</br></br></br>

<b>2. Repayment of liabilities in cash</b></br></br>

&nbsp;&nbsp;&nbsp;&nbsp; a) Decrease in liabilities of organization will be debited.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Debit the account of corresponding creditor.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; b) What goes out of the organization will be debited. </br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Cash goes out of the  organization; cash account will be credited in journal entry.</br></br>

<b><u>Journal Entry</u></br></br>

Sundry creditor's Account Debit 20,000</br></br>

Cash Account Credit 20,000</br></br>

Narration : </b>Payment made to sundry creditors for goods purchased on credit.</br></br>

<font color='blue'>NOTE : The forward reference id of this transaction will point to transaction no. 3</font></br></br></br>


<b>3. Refer assets acquired from a fund</b></br></br>

&nbsp;&nbsp;&nbsp;&nbsp; a) Decrease in liabilities of organization will be debited.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Debit the liabilities account of the fund.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; b) Increase in assets of an organization is considered as income.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Credit the income account of the corresponding fund.</br></br>

<b><u>Journal Entry</u></br></br>

Fund's Liability Account Debit 20,000</br></br>

Fund's Income Account Credit 20,000</br></br>

Narration : </b>Asset purchased.</br></br>

<font color='blue'>NOTE : The backward refernce id of this transaction will pint to transaction no. 2</font></br></br></br></br>


<u><b><h4>CASE III (Asset purchased on cash / Payment)</h4></b></u></br>

<b>1. Asset purchased on cash</b></br></br>

&nbsp;&nbsp;&nbsp;&nbsp; a) What comes in organization will be debited</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Asset have come in organization, so its financial value will be debited with the name of asset's account. </br></br>

&nbsp;&nbsp;&nbsp;&nbsp; b) What goes out of the organization will be debited.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Cash goes out of the  organization; cash account will be credited in journal entry.</br></br>

<b><u>Journal Entry</u></br></br>

Asset's Account Debit 100,000</br></br>

Cash's Account Credit 100,000</br></br>

Narration : </b>Asset purchased on cash.</br></br>

<font color='blue'>NOTE : The forward reference id of this transaction will point to transaction no. 2</font></br></br></br>


<b>2. Refer assets acquired from a fund</b></br></br>

&nbsp;&nbsp;&nbsp;&nbsp; a) Decrease in liabilities of organization will be debited.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Debit the liabilities account of the fund.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; b) Increase in assets of an organization is considered as income.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Credit the income account of the corresponding fund.</br></br>

<b><u>Journal Entry</u></br></br>

Fund's Liability Account Debit 100,000</br></br>

Fund's Income Account Credit 100,000</br></br>

Narration : </b>Asset purchased.</br></br>

<font color='blue'>NOTE : The backward refernce id of this transaction will pint to transaction no. 1</font></br></br></br></br>

<u><b><h4>CASE IV (Accrued Income / Income)</h4></b></u></br>

<b>1. Interest receivable on bank balance</b></br></br>

&nbsp;&nbsp;&nbsp;&nbsp; a) Receivables are considered as assets, hence will be debited.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Specific receivables account will be debited. We'll be receiving interest for this month, in the next one.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; b) Interest on bank balance is an income of organization.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Increase in income will be credited in journal entry.</br></br>

<b><u>Journal Entry</u></br></br>

Interest Income Receivables Account Debit 10,000</br></br>

Interest on bank Deposit Credit 10,000</br></br>

Narration : </b>Interest on bank account to be received.</br></br>

<font color='blue'>NOTE : The forward reference id of this transaction will point to transaction no. 2</font></br></br></br>


<b>2. Interest received on bank balance</b></br></br>

&nbsp;&nbsp;&nbsp;&nbsp; a) Increase in assets of organization will be debited.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Debit the bank's account.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; b) Decrease in liability of an organization will be debited.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Credit the receivables account of the bank.</br></br>

<b><u>Journal Entry</u></br></br>

Bank's Account Debit 10,000</br></br>

Interest Income Receivables Account Credit 10,000</br></br>

Narration : </b>Interest on bank balance received.</br></br>

<font color='blue'>NOTE : The backward refernce id of this transaction will pint to transaction no. 1</font></br></br></br></br>


<u><b><h4>CASE V (Receipt)</h4></b></u></br>

<b>1.  Student's fee received Rs. 50,000</b></br></br>

&nbsp;&nbsp;&nbsp;&nbsp; a) What comes in organization will be debited </br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Cash has come in organization; cash account will be debited in journal entry.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; b) Student fees account will be credited  </br></br>

&nbsp;&nbsp;&nbsp;&nbsp; Fees is considered as income, hence increase in income will be credited.</br></br>

<b><u>Journal Entry</u></br></br>

Cash Account Debit 50, 000</br></br>

Fees Account Credit 50, 000</br></br>

Narration : </b>Fees received from student.</br></br></br></br>



<u><b><h4>CASE VI (Expense)</h4></b></u></br>

<b>1. Salary paid to employees Rs. 100,000</b></br></br>

&nbsp;&nbsp;&nbsp;&nbsp; a) Salary is an item of expense, hence it will be debited</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; But, the entry will be made only when, the related expense head has sufficient budget amount.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; b)  Cash is an item of asset and it goes out, so it will be credited.</br></br>


<b><u>Journal Entry</u></br></br>

Expense Account Debit 100,000</br></br>

Cash's Account Credit 100,000</br></br>

Narration : </b>Salary paid.</br></br></br></br>

<b>2. Salary paid to employees Rs. 100,000 and TDS is being deducted at rate of 1%</b></br></br>

&nbsp;&nbsp;&nbsp;&nbsp; a) Salary is an item of expense, hence it will be debited</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; But, the entry will be made only when, the related expense head has sufficient budget amount.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; b)  Cash is an item of asset and it goes out, so it will be credited.</br></br>

&nbsp;&nbsp;&nbsp;&nbsp; c)  TDS is an item of sources of funds, so it will be credited.</br></br>



<b><u>Journal Entry</u></br></br>

Expense Account Debit 100,000</br></br>

Cash's Account Credit 99,000</br></br>

TDS's Account Credit 1,000</br></br>

Narration : </b>Deduction@1% on Payment Amount 100,000.00 u/s 194C</br></br></br></br>

