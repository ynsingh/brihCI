Related party disclosures<br><br>
Accounting Standard (AS) 18, ‘Related Party Disclosures’, issued by the Institute of Chartered Accountants of India, requires disclosures to be made in respect of related party transactions. Keeping in view the involvement of public funds, in the context of an educational institution, the following disclosures should be made in a note to the financial statements of the educational institution:<br><br>
<ol type="i">
<li>
Transactions between the educational institution and the trust or society managing the educational institution.
</li><li>
Transactions between the educational institution and the trustees or the members of the governing body of the educational institution.
</li><li>
Transactions between the educational institution and the author of the trust or the founder of the institution.
</li><li>
Transactions between the educational institution with another educational institution or any other educational entity managed by the same trust or society, if permitted by the relevant legislation/bye-laws etc.
</li><li>
Transactions between the educational institution and the relatives of the trustees, or members of the governing body managing the educational institution or the author of the trust or the founder of the institution. For this purpose, a relative, in the context of an individual, means “the spouse, son, daughter, brother, sister, father and mother who may be expected to influence, or influenced by, that individual in his/her dealing with the educational institution”.
</li><li>
Transactions between the educational institution and its ‘key management personnel’ or the relatives of the key management personnel. Key management personnel would represent those persons in the educational institution who have the authority and responsibility for planning, directing and controlling the activities of the educational institution. In case of an educational institution, an example of key management personnel is the principal.
</li>
</ol>
<br>If there have been transactions between related parties, during the existence of a related party relationship, the educational institution should disclose the following:<br><br>
<ol type="i">
<li>
the name of the transacting related party;
</li><li>
a description of the relationship between the parties;
</li><li>
a description of the nature of transactions;
</li><li>
volume of the transactions; either as an amount or as an appropriate proportion;
</li><li>
the amounts or appropriate proportions or outstanding items pertaining to related parties at the balance sheet date and provisions for doubtful debts due from such parties at that date; and
</li><li>
amounts written off or written back in the period in respect of debts due from or to related parties. 
</li>
</ol>
<br>
The following are examples of the related party transactions in respect of which disclosures may be made by an educational institution:<br><br>
<ul>
<li>
purchases or sales of fixed assets;
</li><li>
rendering or receiving of services;
</li><li>
leasing or hire purchase arrangements;
</li>
</ul>
<br><br>
Items of a similar nature may be disclosed in aggregate by type of related party except when separate disclosure is necessary for an understanding of the effects of related party transactions on the financial statements of the educational institution.
<br><br>
Disclosure of details of particular transactions with individual related parties would frequently be too voluminous to be easily understood. Accordingly, items of a similar nature may be disclosed in aggregate by type of related party.
<br><br>
However, this is not done in such a way as to obscure the importance of significant transactions, e.g., purchases or sales of books are not aggregated with purchases or sales of fixed assets. Nor a material related party transaction with an individual party is clubbed in an aggregated disclosure. 
</ol>
