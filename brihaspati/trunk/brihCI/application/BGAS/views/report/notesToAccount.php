Educational institutions should disclose their significant accounting policies and this disclosure should be made at one place. An illustrative list of accounting policies that an educational institution could disclose is as follows:<br><br>
<ol type="a">
<li>
The basis of recognition of major types of expenses and revenue
</li><li>
Accounting for income from and expenditure on specialised activities such as research
</li><li>
Conversion or translation of foreign currency (in case of organisations receiving foreign funds)
</li><li>
Method(s) of depreciation
</li><li>
Valuation of inventories
</li><li>
Valuation of investments
</li><li>
Treatment of employee benefits
</li><li>
Valuation of fixed assets
</li><li>
Treatment of contingent liabilities
</li>
</ol>
<br>
</li><li>
In respect of funds, educational institutions should disclose the following in the schedules/notes to accounts: <br><br>
<ol type="a">
<li>
In respect of each major fund, opening balance, additions during the period, deductions/utilisation during the period and balance at the end;
</li><li>
Assets, such as investments, and liabilities belonging to each fund separately;
</li><li>
Restrictions, if any, on the utilisation of each fund balance;
</li><li>
Restrictions, if any, on the utilisation of specific assets.
</li>
</ol>
<br>
